<h1><span style="color:#f1c40f">&iquest;Qu&eacute; es el compost?</span></h1>

<p>El compost es un abono natural lleno de nutrientes que nos sirve para abonar nuestro jard&iacute;n o las plantas que tengamos en casa. Es una gran alternativa que adem&aacute;s respeta el medio ambiente y remplaza el uso de los fertilizantes qu&iacute;micos. <strong>La transformaci&oacute;n de la materia org&aacute;nica se lleva a cabo con el compostaje</strong>. Es un proceso natural realizado por la acci&oacute;n de microorganismos y descomponedores del suelo. No te preocupes no hay malos olores ni putrefacci&oacute;n. De cada kilo de residuos org&aacute;nicos podemos obtener hasta 300 gramos de compost.</p>

<p>&nbsp;</p>

<h1><span style="color:#f1c40f">Sus beneficios</span></h1>

<p><span style="color:#f1c40f"><img class="img-fluid" alt="" src="https://elleonverde.com/uploads/images/content/1620191680-el-leon-verde.png" style="height:183px; width:350px" /></span></p>

<p>Sin dudas el m&aacute;s importante es que estamos aportando <strong>un recurso valioso para nuestros suelos</strong> escasos de materia org&aacute;nica con unos desechos que no se van al basurero y as&iacute; contribuimos a cuidar el medio ambiente. Nuestros desechos tienen otro uso, otra vida.</p>

<p>El compost que cualquiera de nosotros puede hacer desde la comodidad de nuestro hogar sustituye el uso de fertilizantes y plaguicidas qu&iacute;micos, por lo que contribuye a evitar la contaminaci&oacute;n. Una buena gesti&oacute;n de los residuos biodegradables puede proporcionar beneficios ambientales, sociales y econ&oacute;micos.</p>

<p>&nbsp;</p>

<h1><span style="color:#f1c40f">Manos a la obra</span></h1>

<p><strong>&iquest;Qu&eacute; necesitamos?</strong></p>

<p><strong><img class="img-fluid" alt="" src="https://elleonverde.com/uploads/images/content/1620191810-el-leon-verde.png" style="height:209px; width:400px" /></strong></p>

<p>Lo recomendado para hacer compost es <strong>tener un compostador </strong>prefabricado donde apilar los restos org&aacute;nicos y donde se llevar&aacute; a cabo todo el proceso de compostaje. En el mercado los venden de pl&aacute;stico, rejilla o madera y de diferentes tama&ntilde;os. Si quieres hacer uno t&uacute; mismo hay una variedad de tutoriales en la web. Necesitas adem&aacute;s unas tijeras de poda, un rastrillo para airear los materiales o un tamiz para conseguir un compost m&aacute;s fino.</p>

<p>&nbsp;</p>

<h1><span style="color:#f1c40f">Qu&eacute;&nbsp;residuos si y cu&aacute;les no.</span></h1>

<p>Esta cuesti&oacute;n la debemos tener muy clara para conseguir un buen compost con &eacute;xito.</p>

<p><img class="img-fluid" alt="" src="https://elleonverde.com/uploads/images/content/1620191743-el-leon-verde.png" style="height:209px; width:400px" /></p>

<p><strong>S&iacute; se pueden utilizar</strong> los restos de fruta y verdura, flores, plantas, c&aacute;scaras de huevo, restos de poda, tapones de corcho, papel de cocina o servilletas, cenizas, serr&iacute;n de madera, aceite y vinagre, posos de caf&eacute;, restos de infusiones, esti&eacute;rcol y paja.</p>

<p><strong>No debemos echar </strong>al compostador son los restos de pescado, carne y huesos, esti&eacute;rcol de animales dom&eacute;sticos, cenizas, revistas ilustradas, pa&ntilde;ales, serr&iacute;n de maderas tratadas y otros materiales no org&aacute;nicos.</p>

<p>&nbsp;</p>

<h1><span style="color:#f1c40f">&iquest;C&oacute;mo agrupamos los residuos?</span></h1>

<p><strong>Los materiales h&uacute;medos</strong> (los ricos en agua y nutrientes como el nitr&oacute;geno) que incluyen los restos de frutas, verduras, c&eacute;sped fresco, etc.</p>

<p><strong>Los materiales secos</strong> (los compuestos b&aacute;sicamente por carbono) como ramas, hojas secas, cart&oacute;n, etc. Tener agrupados los residuos org&aacute;nicos nos ayudar&aacute; a saber qu&eacute; cantidad de cada uno debemos echar en el compostador. La f&oacute;rmula perfecta seg&uacute;n Amigos de la Tierra es mantener la proporci&oacute;n de dos partes de materiales h&uacute;medos por una de materiales secos con el fin de garantizar una buena humedad.</p>

<p>&nbsp;</p>

<p><strong>Un error muy com&uacute;n</strong> a la hora de hacer compost casero es la inadecuada mezcla de restos vegetales le&ntilde;osos de jard&iacute;n que aportan carbono y los restos vegetales de cocina que aportan nitr&oacute;geno.</p>

<p>&nbsp;</p>

<h1><span style="color:#f1c40f">Las claves de un buen compost casero</span></h1>

<p>El primer llenado del compostador deber&aacute; llevar un lecho en el fondo formado por material le&ntilde;oso como ramas gruesas y pi&ntilde;as para facilitar la circulaci&oacute;n de aire (unos 30 cent&iacute;metros aproximadamente). Despu&eacute;s apilaremos los residuos org&aacute;nicos en diferentes capas, mezclados con compost ya acabado para asegurar que el contenedor se inocule con microorganismos y a&ntilde;adiendo una capa de esti&eacute;rcol de animal de granja para <strong>acelerar el proceso de descomposici&oacute;n.</strong></p>

<p><strong>De forma peri&oacute;dica a&ntilde;adiremos m&aacute;s residuos</strong>. Mejor cuanto m&aacute;s peque&ntilde;os sean los trozos para que bacterias y microorganismos puedan actuar y la descomposici&oacute;n sea m&aacute;s r&aacute;pida. Es importante proteger el compostador de cambios bruscos de temperatura y remover el contenido siempre que sea posible para asegurar que los organismos descomponedores puedan respirar y no se den procesos de fermentaci&oacute;n por ausencia de aire.</p>

<p><strong>Entre seis y ocho meses</strong> despu&eacute;s del inicio del proceso, podemos comenzar a extraer nuestro compost casero del fondo del contenedor. Sabremos que est&aacute; maduro por su agradable olor a tierra de bosque. Tras dejarlo reposar unos d&iacute;as, ya lo tendremos listo para abonar nuestras plantas con los mejores nutrientes.</p>

